    <script>
        var current_device = '<?=$current_device?>';
        var current_role   = '<?=$current_role;?>';
        var app_base_url   = '<?=base_url();?>';
        function getNotification(){
            if(current_role != 'admin' && current_device == 'desktop'){
                $.ajax({
                    type: 'GET',
                    dataType: 'json',
                    url: '<?=base_url('general/getNotification');?>',
                    data: {},
                    success: function(data) {
                        if(data.status == 1){
                            if(confirm(data.msg) == true) {
                                if(current_role == 'rider'){
                                    window.location.href = app_base_url+'rider/currentOrder';
                                }else{
                                    if(data.order_type == 'donate'){
                                        window.location.href = app_base_url+'panel/viewDonationOrder/'+data.order_id;
                                    }else{
                                        window.location.href = app_base_url+'panel/viewRequestOrder/'+data.order_id;
                                    }
                                }
                            }else{
                                location.reload();
                            }
                        }
                    }
                });
            }
        } //getNotification

        // $(document).ready(function(){
